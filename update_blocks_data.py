def update_blocks_data_js(file_path):
    """blocks-data.js 파일의 objectives 업데이트"""
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # 각 블록의 objectives 찾아서 교체
    for block_id in range(1, 18):
        # objectives 배열 패턴 찾기
        pattern = rf'(id: {block_id},.*?objectives: \[)(.*?)(\],)'
        
        def replace_objectives(match):
            new_objectives = ',\n            '.join([f'"{obj}"' for obj in OBJECTIVES[block_id]])
            return match.group(1) + '\n            ' + new_objectives + '\n        ' + match.group(3)
        
        content = re.sub(pattern, replace_objectives, content, flags=re.DOTALL)
    
    with open(file_path, 'w', encoding='utf-8') as f:
        f.write(content)
    
    return True

